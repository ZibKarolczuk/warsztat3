package pl.coderslab.warsztat3.model;

public class UserGroup {

    public UserGroup() {
        this.id = 0;
    }

    public UserGroup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
